"""Maximum Entropy Spectral Analysis (MESA) strategy -- Burg's method.

Uses Burg's maximum-entropy method (MEM) to fit autoregressive models and
extract high-resolution power spectra from short price windows.  Dominant
market cycles are identified from spectral peaks, and trading signals are
generated by tracking the instantaneous phase of the dominant cycle via the
Hilbert transform of a bandpass-filtered price series.

Mathematical foundation
-----------------------
Maximum Entropy Principle:
    Among all power spectral densities S(f) consistent with the first p
    autocorrelation lags, choose the one that maximises the spectral entropy

        H = integral  log S(f) df

    This is equivalent to fitting an autoregressive model of order p whose
    prediction-error power equals the entropy-maximising spectrum.

Burg's algorithm:
    1.  Initialise forward/backward prediction errors as the centred data.
    2.  For m = 1, ..., p:
        a.  Compute the reflection coefficient k_m that minimises the total
            (forward + backward) prediction-error power (Levinson-Durbin
            lattice update).
        b.  Update the AR coefficients via the Levinson recursion.
        c.  Update forward/backward errors.
    3.  The maximum-entropy PSD is then

            S(f) = P_p / |1 + sum_{k=1}^{p} a_k e^{-j2pi f k}|^2

Advantages over FFT-based spectral estimation:
    *   Much better frequency resolution for short data windows (50--100 pts).
    *   No windowing artifacts (spectral leakage / sidelobes).
    *   Naturally extrapolates beyond the data length.
    *   Spectral peaks are sharper, enabling more precise cycle detection.

Strategy logic
--------------
1.  MESA Cycle Detection -- apply Burg's method on a rolling window of
    log-returns; compute S(f); identify dominant cycle period T_dom from the
    highest spectral peak.
2.  Dominant Cycle Trading -- bandpass-filter around T_dom, apply Hilbert
    transform to get instantaneous phase; buy at cycle trough (phase ~ -pi),
    sell at cycle peak (phase ~ 0).
3.  Cycle Stability -- track variance of T_dom over recent windows; stable
    cycles yield higher-confidence (larger) positions.
4.  Multi-Cycle Overlay -- when multiple significant peaks exist, combine
    signals weighted by their spectral power.
"""

from __future__ import annotations

import logging
from typing import Any, Dict, List, Optional, Tuple

import numpy as np
import pandas as pd
from scipy.signal import butter, sosfiltfilt, hilbert

from src.strategies.base import Strategy

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Burg's Maximum Entropy Method -- from scratch
# ---------------------------------------------------------------------------

def _burg_ar(
    x: np.ndarray,
    order: int,
) -> Tuple[np.ndarray, float]:
    """Estimate AR coefficients using Burg's method (Levinson-Durbin lattice).

    Implements the algorithm described in:
        S.L. Marple, *Digital Spectral Analysis with Applications*,
        Prentice Hall, 1987, Chapter 8.

    The AR model convention used here is::

        x[n] + a[0]*x[n-1] + a[1]*x[n-2] + ... + a[p-1]*x[n-p] = e[n]

    so that the power spectral density is::

        S(f) = pm / |1 + sum_{k=1}^{p} a[k-1] * exp(-j*2*pi*f*k)|^2

    Parameters
    ----------
    x : 1-D array
        Zero-mean input signal of length N.
    order : int
        AR model order p.

    Returns
    -------
    a : 1-D array of length *order*
        AR coefficients [a_1, a_2, ..., a_p] in the convention above.
    pm : float
        Prediction error power (variance of the innovations).

    Raises
    ------
    ValueError
        If *order* >= len(x) or data is degenerate.

    Notes
    -----
    The reflection coefficients are computed to jointly minimise the sum
    of forward and backward prediction-error powers -- this is the
    distinguishing feature of Burg's algorithm versus other AR estimators
    (e.g. Yule-Walker, covariance method).  The denominator is updated
    incrementally for numerical efficiency.
    """
    x = np.asarray(x, dtype=np.float64)
    n = len(x)
    if order >= n:
        raise ValueError(
            f"AR order ({order}) must be less than data length ({n})."
        )
    if order == 0:
        return np.array([], dtype=np.float64), float(np.dot(x, x) / n)

    # Initialise forward and backward prediction errors
    ef = x[1:n].copy()   # forward errors:  ef[i] = x[i+1]
    eb = x[0:n-1].copy() # backward errors: eb[i] = x[i]

    # AR coefficients built up iteratively
    a = np.zeros(order, dtype=np.float64)

    # Denominator for the reflection coefficient (updated incrementally)
    den = float(np.dot(ef, ef) + np.dot(eb, eb))

    # Total squared error (= n * prediction_error_power)
    err = float(np.dot(x, x))

    for m in range(order):
        # --- Reflection coefficient k_{m+1} ---
        num = -2.0 * float(np.dot(eb, ef))

        if abs(den) < 1e-30:
            break

        k = num / den

        # Stability check: |k| must be < 1 for a stable AR model
        if abs(k) >= 1.0:
            break

        # Update total error
        err *= (1.0 - k * k)

        # --- Levinson-Durbin recursion for AR coefficients ---
        if m == 0:
            a[0] = k
        else:
            a_prev = a[:m].copy()
            a[:m] = a_prev + k * a_prev[::-1]
            a[m] = k

        # --- Update forward/backward errors (lattice step) ---
        if len(ef) <= 1:
            break
        ef_old = ef.copy()
        ef = ef_old[1:] + k * eb[1:]
        eb = eb[:-1] + k * ef_old[:-1]

        # Incremental denominator update (avoids O(N) recomputation)
        den = (1.0 - k * k) * den - ef_old[0] ** 2 - eb[-1] ** 2

        # Safety: if incremental update goes non-positive due to
        # numerical issues, recompute from scratch
        if den <= 0:
            den = float(np.dot(ef, ef) + np.dot(eb, eb))

    pm = err / n
    return a, pm


def _mesa_spectrum(
    a: np.ndarray,
    pm: float,
    n_freqs: int = 512,
    dt: float = 1.0,
) -> Tuple[np.ndarray, np.ndarray]:
    """Compute the maximum entropy power spectral density from AR coefficients.

    Parameters
    ----------
    a : 1-D array
        AR coefficients [a_1, ..., a_p].
    pm : float
        Prediction error power.
    n_freqs : int
        Number of frequency bins in [0, f_Nyquist].
    dt : float
        Sampling interval (1.0 for daily data).

    Returns
    -------
    freqs : 1-D array of length *n_freqs*
        Frequency values in [0, 0.5/dt].
    psd : 1-D array of length *n_freqs*
        Power spectral density at each frequency.

    Notes
    -----
    Uses the Burg convention where the AR model is
    ``x[n] + a_1 x[n-1] + ... = e[n]``, giving::

        S(f) = (pm * dt) / |1 + sum_{k=1}^{p} a_k e^{-j 2 pi f k dt}|^2
    """
    freqs = np.linspace(0, 0.5 / dt, n_freqs, endpoint=False)
    p = len(a)
    if p == 0:
        return freqs, np.full(n_freqs, pm * dt)

    # Build the AR polynomial denominator evaluated at each frequency
    # A(f) = 1 + sum_{k=1}^{p} a_k * exp(-j * 2*pi * f * k * dt)
    k_idx = np.arange(1, p + 1)  # shape (p,)
    # Outer product: phase matrix (n_freqs, p)
    phase = -2.0 * np.pi * dt * np.outer(freqs, k_idx)
    # A(f) for each frequency
    ar_poly = 1.0 + np.sum(a[np.newaxis, :] * np.exp(1j * phase), axis=1)

    psd = (pm * dt) / np.abs(ar_poly) ** 2
    return freqs, psd


# ---------------------------------------------------------------------------
# Model order selection via AIC
# ---------------------------------------------------------------------------

def _select_ar_order(
    x: np.ndarray,
    max_order: int,
    criterion: str = "aic",
) -> int:
    """Select optimal AR order by minimising information criterion.

    Parameters
    ----------
    x : 1-D array
        Zero-mean input signal.
    max_order : int
        Maximum AR order to consider.
    criterion : {'aic', 'bic'}
        Information criterion.

    Returns
    -------
    int
        Selected AR model order (>= 1).
    """
    n = len(x)
    best_order = 1
    best_ic = np.inf

    for p in range(1, max_order + 1):
        try:
            _, pm = _burg_ar(x, p)
        except ValueError:
            break
        if pm <= 0:
            pm = 1e-30

        log_pm = np.log(pm)
        if criterion == "aic":
            ic = n * log_pm + 2.0 * p
        else:  # BIC
            ic = n * log_pm + p * np.log(n)

        if ic < best_ic:
            best_ic = ic
            best_order = p

    return best_order


# ---------------------------------------------------------------------------
# Spectral peak detection
# ---------------------------------------------------------------------------

def _find_spectral_peaks(
    freqs: np.ndarray,
    psd: np.ndarray,
    min_period: float = 5.0,
    max_period: float = 200.0,
    peak_to_bg_threshold: float = 3.0,
    max_peaks: int = 5,
) -> List[Dict[str, float]]:
    """Identify significant peaks in the MESA power spectrum.

    Parameters
    ----------
    freqs : 1-D array
        Frequency values (cycles per sample).
    psd : 1-D array
        Power spectral density.
    min_period : float
        Minimum cycle period (in samples) to consider.
    max_period : float
        Maximum cycle period (in samples) to consider.
    peak_to_bg_threshold : float
        Minimum ratio of peak power to median (background) power for a peak
        to be considered significant.
    max_peaks : int
        Maximum number of peaks to return.

    Returns
    -------
    list of dict
        Each dict has keys 'frequency', 'period', 'power', 'peak_ratio'.
        Sorted by descending power.
    """
    # Convert period bounds to frequency bounds
    f_min = 1.0 / max_period if max_period > 0 else 0.0
    f_max = 1.0 / min_period if min_period > 0 else 0.5

    # Mask to valid frequency range (exclude DC)
    valid = (freqs > f_min) & (freqs <= f_max) & (freqs > 0)
    if not np.any(valid):
        return []

    valid_freqs = freqs[valid]
    valid_psd = psd[valid]

    # Background level: median power in the valid range
    bg_power = np.median(valid_psd)
    if bg_power <= 0:
        bg_power = 1e-30

    # Simple peak detection: local maxima that exceed threshold
    peaks = []
    for i in range(1, len(valid_psd) - 1):
        if valid_psd[i] > valid_psd[i - 1] and valid_psd[i] > valid_psd[i + 1]:
            ratio = valid_psd[i] / bg_power
            if ratio >= peak_to_bg_threshold:
                peaks.append({
                    "frequency": float(valid_freqs[i]),
                    "period": float(1.0 / valid_freqs[i]),
                    "power": float(valid_psd[i]),
                    "peak_ratio": float(ratio),
                })

    # Sort by power (descending) and keep top max_peaks
    peaks.sort(key=lambda p: p["power"], reverse=True)
    return peaks[:max_peaks]


# ---------------------------------------------------------------------------
# Bandpass filter + Hilbert phase extraction
# ---------------------------------------------------------------------------

def _bandpass_filter(
    x: np.ndarray,
    center_period: float,
    bandwidth_fraction: float = 0.3,
    dt: float = 1.0,
    filter_order: int = 3,
) -> np.ndarray:
    """Apply a Butterworth bandpass filter centred on *center_period*.

    Parameters
    ----------
    x : 1-D array
        Input signal.
    center_period : float
        Centre period of the bandpass (in samples).
    bandwidth_fraction : float
        Fractional bandwidth around the centre frequency.  E.g. 0.3 means
        the passband spans [0.7*f_c, 1.3*f_c].
    dt : float
        Sampling interval.
    filter_order : int
        Butterworth filter order.

    Returns
    -------
    1-D array
        Bandpass-filtered signal (same length as *x*).
    """
    f_center = 1.0 / center_period
    f_low = f_center * (1.0 - bandwidth_fraction)
    f_high = f_center * (1.0 + bandwidth_fraction)

    # Nyquist frequency
    f_nyq = 0.5 / dt

    # Normalise to [0, 1] range for scipy
    low = max(f_low / f_nyq, 1e-6)
    high = min(f_high / f_nyq, 1.0 - 1e-6)

    if low >= high:
        return np.zeros_like(x)

    try:
        sos = butter(filter_order, [low, high], btype="band", output="sos")
        filtered = sosfiltfilt(sos, x)
    except (ValueError, np.linalg.LinAlgError):
        return np.zeros_like(x)

    return filtered


def _hilbert_phase(signal: np.ndarray) -> np.ndarray:
    """Extract instantaneous phase via the Hilbert transform.

    Returns
    -------
    phase : 1-D array
        Unwrapped instantaneous phase in radians.
    """
    analytic = hilbert(signal)
    phase = np.unwrap(np.angle(analytic))
    return phase


# ---------------------------------------------------------------------------
# Polynomial detrending
# ---------------------------------------------------------------------------

def _poly_detrend(x: np.ndarray, order: int = 3) -> np.ndarray:
    """Remove polynomial trend of given order from *x* and centre to zero mean.

    Using a cubic (order=3) polynomial removes slow trends and low-frequency
    curvature that would otherwise dominate the AR model and cause the
    reflection coefficients to saturate near +/-1, breaking Burg's method.

    Parameters
    ----------
    x : 1-D array
        Input signal.
    order : int
        Polynomial order for detrending (default 3 = cubic).

    Returns
    -------
    1-D array
        Detrended, zero-mean signal.
    """
    n = len(x)
    t = np.arange(n, dtype=np.float64)
    coeffs = np.polyfit(t, x, order)
    trend = np.polyval(coeffs, t)
    detrended = x - trend
    detrended -= detrended.mean()
    return detrended


# ---------------------------------------------------------------------------
# Cycle stability measurement
# ---------------------------------------------------------------------------

def _cycle_stability(
    dominant_periods: np.ndarray,
    lookback: int = 10,
) -> np.ndarray:
    """Compute rolling coefficient of variation of the dominant cycle period.

    A low CV indicates a stable, reliable cycle.

    Parameters
    ----------
    dominant_periods : 1-D array
        Time series of dominant cycle periods (may contain NaN).
    lookback : int
        Rolling window for CV calculation.

    Returns
    -------
    stability : 1-D array
        Values in [0, 1] where 1 = perfectly stable cycle, 0 = unstable.
        NaN where insufficient data.
    """
    n = len(dominant_periods)
    stability = np.full(n, np.nan)
    for i in range(lookback - 1, n):
        window = dominant_periods[i - lookback + 1 : i + 1]
        valid = window[~np.isnan(window)]
        if len(valid) < lookback // 2:
            continue
        mean_p = np.mean(valid)
        if mean_p < 1e-10:
            continue
        cv = np.std(valid) / mean_p
        # Map CV to stability: CV=0 -> 1.0, CV=1 -> ~0.27
        stability[i] = np.exp(-cv * cv)
    return stability


