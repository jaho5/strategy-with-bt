class MaxEntropySpectrumStrategy(Strategy):
    """Maximum Entropy Spectral Analysis (MESA) trading strategy.

    Uses Burg's method to fit high-resolution AR spectral models on short
    rolling windows of log-returns, identifies dominant market cycles from
    spectral peaks, and trades the phase of those cycles.

    Parameters
    ----------
    window : int
        Rolling window (trading days) for the MESA analysis.  Burg's method
        works well with windows as small as 50--100 samples.
    max_ar_order : int
        Maximum AR model order to evaluate.  The actual order is selected
        by minimising AIC.
    n_freqs : int
        Number of frequency bins for the computed spectrum.
    min_period : float
        Minimum cycle period (days) to consider as a tradeable cycle.
    max_period : float
        Maximum cycle period (days) to consider.
    peak_threshold : float
        Minimum peak-to-background power ratio for a spectral peak to be
        considered significant.
    bandwidth_frac : float
        Fractional bandwidth of the bandpass filter around the dominant
        cycle frequency.
    stability_lookback : int
        Number of recent windows used to assess cycle stability (CV).
    stability_min : float
        Minimum stability score [0, 1] required to trade.  Set to 0 to
        disable the stability filter.
    phase_buy : float
        Phase (radians) at which to go long.  Default -pi (cycle trough).
    phase_sell : float
        Phase (radians) at which to go short.  Default 0 (cycle peak).
    phase_tolerance : float
        Half-width of the phase window around buy/sell phases (radians).
    max_cycles : int
        Maximum number of spectral peaks to overlay in the multi-cycle
        composite signal.
    """

    def __init__(
        self,
        window: int = 80,
        max_ar_order: int = 30,
        n_freqs: int = 512,
        min_period: float = 5.0,
        max_period: float = 120.0,
        peak_threshold: float = 3.0,
        bandwidth_frac: float = 0.3,
        stability_lookback: int = 10,
        stability_min: float = 0.2,
        phase_buy: float = -np.pi,
        phase_sell: float = 0.0,
        phase_tolerance: float = np.pi / 4,
        max_cycles: int = 3,
    ) -> None:
        super().__init__(
            name="MaxEntropySpectrum",
            description=(
                "Maximum Entropy Spectral Analysis (MESA) strategy using "
                "Burg's method for high-resolution cycle detection and "
                "phase-based trading."
            ),
        )
        self.window = window
        self.max_ar_order = max_ar_order
        self.n_freqs = n_freqs
        self.min_period = min_period
        self.max_period = max_period
        self.peak_threshold = peak_threshold
        self.bandwidth_frac = bandwidth_frac
        self.stability_lookback = stability_lookback
        self.stability_min = stability_min
        self.phase_buy = phase_buy
        self.phase_sell = phase_sell
        self.phase_tolerance = phase_tolerance
        self.max_cycles = max_cycles

        # Populated by fit()
        self._fitted_params: Dict[str, Any] = {}

    # -----------------------------------------------------------------
    # Core MESA analysis for a single window
    # -----------------------------------------------------------------

    def _analyse_window(
        self, x: np.ndarray
    ) -> Tuple[Optional[int], np.ndarray, np.ndarray, List[Dict[str, float]]]:
        """Run Burg's method and spectral peak detection on a single window.

        Parameters
        ----------
        x : 1-D array
            Zero-mean log-return (or detrended log-price) window.

        Returns
        -------
        ar_order : int or None
            Selected AR model order (None if analysis failed).
        freqs : 1-D array
            Frequency grid.
        psd : 1-D array
            Power spectral density.
        peaks : list of peak dicts
            Significant spectral peaks found.
        """
        n = len(x)
        max_p = min(self.max_ar_order, n // 2 - 1)
        if max_p < 1:
            return None, np.array([]), np.array([]), []

        try:
            ar_order = _select_ar_order(x, max_p)
            a, pm = _burg_ar(x, ar_order)
        except (ValueError, FloatingPointError):
            return None, np.array([]), np.array([]), []

        freqs, psd = _mesa_spectrum(a, pm, n_freqs=self.n_freqs)
        peaks = _find_spectral_peaks(
            freqs,
            psd,
            min_period=self.min_period,
            max_period=self.max_period,
            peak_to_bg_threshold=self.peak_threshold,
            max_peaks=self.max_cycles,
        )
        return ar_order, freqs, psd, peaks

    # -----------------------------------------------------------------
    # Single-asset signal generation (rolling)
    # -----------------------------------------------------------------

    def _generate_single_asset_signals(
        self,
        prices: pd.Series,
    ) -> Tuple[pd.Series, pd.Series]:
        """Produce signal and weight series for one asset.

        Returns
        -------
        signal : pd.Series
            Values in {-1, 0, +1}.
        weight : pd.Series
            Position-sizing weight in [0, 1].
        """
        n = len(prices)
        price_vals = prices.values.astype(np.float64)

        # Guard against non-positive prices
        price_vals = np.where(price_vals <= 0, np.nan, price_vals)
        log_p = np.log(price_vals)

        signal_out = np.zeros(n)
        weight_out = np.zeros(n)

        # Track dominant period over time for stability calculation
        dom_period_history = np.full(n, np.nan)

        w = self.window

        for t in range(w, n):
            window_data = log_p[t - w : t]
            if np.any(np.isnan(window_data)):
                continue

            # Detrend: remove polynomial trend for spectral analysis.
            # Cubic detrending removes slow curvature that would otherwise
            # cause Burg's reflection coefficients to saturate near +/-1.
            detrended = _poly_detrend(window_data, order=3)

            # Run MESA analysis
            _, _, _, peaks = self._analyse_window(detrended)
            if not peaks:
                continue

            # Record dominant period
            dom_period_history[t] = peaks[0]["period"]

            # --- Multi-cycle composite signal ---
            # Weight each cycle's contribution by its spectral power
            total_power = sum(pk["power"] for pk in peaks)
            if total_power <= 0:
                continue

            composite_signal = 0.0
            total_weight = 0.0

            for pk in peaks:
                period = pk["period"]
                power_weight = pk["power"] / total_power

                # Need enough data for bandpass filtering (at least 3 cycles)
                min_required = max(int(3 * period), w)
                lookback_start = max(0, t - min_required)
                bp_data = log_p[lookback_start : t]

                if len(bp_data) < int(2 * period):
                    # Not enough data for this cycle -- skip
                    continue

                # Detrend the longer segment for filtering
                bp_detrended = _poly_detrend(bp_data, order=3)

                # Bandpass filter around the cycle frequency
                filtered = _bandpass_filter(
                    bp_detrended,
                    center_period=period,
                    bandwidth_fraction=self.bandwidth_frac,
                )

                if np.all(np.abs(filtered) < 1e-15):
                    continue

                # Extract phase at the current point (last sample)
                phase = _hilbert_phase(filtered)
                current_phase = phase[-1]

                # Wrap phase to [-pi, pi]
                current_phase = (current_phase + np.pi) % (2 * np.pi) - np.pi

                # Phase-based signal:
                # Buy zone:  phase near phase_buy (trough, default -pi)
                # Sell zone: phase near phase_sell (peak, default 0)
                buy_dist = min(
                    abs(current_phase - self.phase_buy),
                    abs(current_phase - self.phase_buy - 2 * np.pi),
                    abs(current_phase - self.phase_buy + 2 * np.pi),
                )
                sell_dist = min(
                    abs(current_phase - self.phase_sell),
                    abs(current_phase - self.phase_sell - 2 * np.pi),
                    abs(current_phase - self.phase_sell + 2 * np.pi),
                )

                if buy_dist <= self.phase_tolerance:
                    cycle_signal = 1.0
                elif sell_dist <= self.phase_tolerance:
                    cycle_signal = -1.0
                else:
                    cycle_signal = 0.0

                composite_signal += power_weight * cycle_signal
                total_weight += power_weight

            if total_weight <= 0:
                continue

            composite_signal /= total_weight

            # Discretise composite signal
            if composite_signal > 0.3:
                signal_out[t] = 1.0
            elif composite_signal < -0.3:
                signal_out[t] = -1.0
            else:
                signal_out[t] = 0.0

        # --- Cycle stability weighting ---
        stability = _cycle_stability(
            dom_period_history, lookback=self.stability_lookback
        )

        for t in range(n):
            if signal_out[t] == 0.0:
                weight_out[t] = 0.0
                continue

            stab = stability[t] if not np.isnan(stability[t]) else 0.0
            if stab < self.stability_min:
                # Cycle too unstable -- suppress signal
                signal_out[t] = 0.0
                weight_out[t] = 0.0
            else:
                # Weight proportional to stability
                weight_out[t] = np.clip(stab, 0.0, 1.0)

        signal_series = pd.Series(signal_out, index=prices.index, name="signal")
        weight_series = pd.Series(weight_out, index=prices.index, name="weight")
        return signal_series, weight_series

    # -----------------------------------------------------------------
    # Public interface
    # -----------------------------------------------------------------

    def fit(self, prices: pd.DataFrame, **kwargs: Any) -> "MaxEntropySpectrumStrategy":
        """Calibrate strategy parameters on historical price data.

        Calibration steps:
        1.  Run MESA analysis on the training data to determine the
            distribution of dominant cycle periods per asset.
        2.  Store the median dominant period and optimal AR order to use
            as priors / starting points for live signal generation.
        3.  Validate that meaningful cycles exist in the training data.

        Parameters
        ----------
        prices : pd.DataFrame
            Historical price data (columns = tickers, DatetimeIndex).

        Returns
        -------
        self
        """
        self.validate_prices(prices)

        fitted_info: Dict[str, Dict[str, Any]] = {}

        for col in prices.columns:
            series = prices[col].dropna()
            if len(series) < self.window + self.stability_lookback:
                logger.info(
                    "Skipping %s during fit: insufficient data (%d < %d).",
                    col,
                    len(series),
                    self.window + self.stability_lookback,
                )
                continue

            price_vals = series.values.astype(np.float64)
            price_vals = np.where(price_vals <= 0, np.nan, price_vals)
            log_p = np.log(price_vals)

            # Collect dominant periods and AR orders across the training set
            dom_periods: List[float] = []
            ar_orders: List[int] = []

            step = max(self.window // 4, 1)
            for t in range(self.window, len(log_p), step):
                window_data = log_p[t - self.window : t]
                if np.any(np.isnan(window_data)):
                    continue

                detrended = _poly_detrend(window_data, order=3)

                ar_order, _, _, peaks = self._analyse_window(detrended)
                if ar_order is not None:
                    ar_orders.append(ar_order)
                if peaks:
                    dom_periods.append(peaks[0]["period"])

            info: Dict[str, Any] = {
                "n_windows_analysed": len(ar_orders),
                "cycles_found": len(dom_periods),
            }

            if dom_periods:
                info["median_dominant_period"] = float(np.median(dom_periods))
                info["mean_dominant_period"] = float(np.mean(dom_periods))
                info["std_dominant_period"] = float(np.std(dom_periods))
            if ar_orders:
                info["median_ar_order"] = int(np.median(ar_orders))

            fitted_info[col] = info

        self._fitted_params = fitted_info
        self.parameters = {
            "window": self.window,
            "max_ar_order": self.max_ar_order,
            "peak_threshold": self.peak_threshold,
            "fitted_assets": list(fitted_info.keys()),
        }
        self._fitted = True
        logger.info(
            "MaxEntropySpectrum fitted on %d asset(s).",
            len(fitted_info),
        )
        return self

    def generate_signals(
        self, prices: pd.DataFrame, **kwargs: Any
    ) -> pd.DataFrame:
        """Generate trading signals from price data.

        For each asset, runs rolling MESA analysis, identifies dominant
        cycles, and produces phase-based signals weighted by cycle
        stability.

        Parameters
        ----------
        prices : pd.DataFrame
            OHLCV or adjusted-close price data (columns = tickers,
            DatetimeIndex).

        Returns
        -------
        pd.DataFrame
            Contains ``{ticker}_signal`` and ``{ticker}_weight`` columns
            for each ticker, or ``signal`` / ``weight`` for single-ticker
            data.
        """
        self.validate_prices(prices)

        result = pd.DataFrame(index=prices.index)

        if prices.ndim == 1 or len(prices.columns) == 1:
            # Single asset
            series = prices.iloc[:, 0] if prices.ndim == 2 else prices
            sig, wt = self._generate_single_asset_signals(series)
            result["signal"] = sig
            result["weight"] = wt
        else:
            # Multiple assets
            for col in prices.columns:
                series = prices[col].dropna()
                if len(series) < self.window:
                    result[f"{col}_signal"] = 0.0
                    result[f"{col}_weight"] = 0.0
                    continue

                sig, wt = self._generate_single_asset_signals(series)
                # Reindex to full date range
                result[f"{col}_signal"] = sig.reindex(prices.index, fill_value=0.0)
                result[f"{col}_weight"] = wt.reindex(prices.index, fill_value=0.0)

        return result
